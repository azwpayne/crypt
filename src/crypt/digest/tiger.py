def _round(a: int, b: int, c: int, x: int, mult: int) -> tuple[int, int, int]:
  """Single round of Tiger compression.

  Args:
      a: First state word
      b: Second state word
      c: Third state word
      x: Message word to mix in
      mult: Multiplier for this pass

  Returns:
      Updated (a, b, c) state
  """
  # Ensure x is 64-bit
  x = x & 0xFFFFFFFFFFFFFFFF
  c ^= x
  # Extract bytes from c for S-box lookups
  c0 = c & 0xFF
  c1 = (c >> 8) & 0xFF
  c2 = (c >> 16) & 0xFF
  c3 = (c >> 24) & 0xFF
  c4 = (c >> 32) & 0xFF
  c5 = (c >> 40) & 0xFF
  c6 = (c >> 48) & 0xFF
  c7 = (c >> 56) & 0xFF

  # First S-box lookup
  a = (a - (_S0[c0] ^ _S1[c2] ^ _S2[c4] ^ _S3[c6])) & 0xFFFFFFFFFFFFFFFF

  # Second S-box lookup with different byte positions
  b = (b + (_S3[c1] ^ _S2[c3] ^ _S1[c5] ^ _S0[c7])) & 0xFFFFFFFFFFFFFFFF
  b = (b * mult) & 0xFFFFFFFFFFFFFFFF

  return a, b, c


def _key_schedule(words: tuple[int, ...]) -> tuple[int, ...]:
  """Generate key schedule for Tiger.

  Tiger uses a specific key schedule to expand 8 input words to 24 round words.

  Args:
      words: 8 input words (64-bit each)

  Returns:
      24 scheduled words
  """
  x = [w & 0xFFFFFFFFFFFFFFFF for w in words]
  schedule = []

  # Pass 1: use x as-is
  schedule.extend(x)

  # Generate x for pass 2: x[i] = (x[i-8] - x[i-1]) * mult
  mult = 7
  for i in range(8, 16):
    diff = (x[i - 8] - x[i - 1]) & 0xFFFFFFFFFFFFFFFF
    val = (diff * mult) & 0xFFFFFFFFFFFFFFFF
    x.append(val)
  schedule.extend(x[8:16])

  # Generate x for pass 3
  mult = 9
  for i in range(16, 24):
    diff = (x[i - 8] - x[i - 1]) & 0xFFFFFFFFFFFFFFFF
    val = (diff * mult) & 0xFFFFFFFFFFFFFFFF
    x.append(val)
  schedule.extend(x[16:24])

  return tuple(schedule)


def _compress_block(a: int, b: int, c: int, block: bytes) -> tuple[int, int, int]:
  """Compress a 64-byte block into the Tiger state.

  Args:
      a: First state word
      b: Second state word
      c: Third state word
      block: 64-byte message block

  Returns:
      Updated (a, b, c) state
  """
  # Unpack block into 8 64-bit words (little-endian)
  words = struct.unpack("<8Q", block)

  # Save old state for feedforward
  aa, bb, cc = a, b, c

  # Generate key schedule and ensure all values are 64-bit
  schedule = [s & 0xFFFFFFFFFFFFFFFF for s in _key_schedule(words)]

  # Pass 1 (multiplier = 5)
  mult = 5
  for i in range(8):
    a, b, c = _round(a, b, c, schedule[i], mult)

  # Pass 2 (multiplier = 7)
  mult = 7
  for i in range(8, 16):
    a, b, c = _round(a, b, c, schedule[i], mult)

  # Pass 3 (multiplier = 9)
  mult = 9
  for i in range(16, 24):
    a, b, c = _round(a, b, c, schedule[i], mult)

  # Feedforward: add old state
  a = (a ^ aa) & 0xFFFFFFFFFFFFFFFF
  b = (b - bb) & 0xFFFFFFFFFFFFFFFF
  c = (c + cc) & 0xFFFFFFFFFFFFFFFF

  return a, b, c


def _pad_message_tiger(message: bytes) -> bytes:
  """Pad message for Tiger hash.

  Tiger uses 0x01 as the first padding byte (unlike MD5's 0x80).

  Args:
      message: Raw input bytes

  Returns:
      Padded message
  """
  original_length_bits = len(message) * 8

  # Append 0x01 byte
  message = message + b"\x01"

  # Pad with zeros until length ≡ 56 (mod 64)
  padding_len = (56 - len(message)) % 64
  message = message + b"\x00" * padding_len

  # Append original length as 64-bit little-endian
  return message + struct.pack("<Q", original_length_bits)


def _pad_message_tiger2(message: bytes) -> bytes:
  """Pad message for Tiger2 hash.

  Tiger2 uses 0x80 as the first padding byte (like MD5).

  Args:
      message: Raw input bytes

  Returns:
      Padded message
  """
  original_length_bits = len(message) * 8

  # Append 0x80 byte (like MD5)
  message = message + b"\x80"

  # Pad with zeros until length ≡ 56 (mod 64)
  padding_len = (56 - len(message)) % 64
  message = message + b"\x00" * padding_len

  # Append original length as 64-bit little-endian
  return message + struct.pack("<Q", original_length_bits)


def _tiger_hash(data: bytes | str, *, tiger2_mode: bool = False) -> str:
  """Compute Tiger or Tiger2 hash of input data.

  Args:
      data: Input data (bytes or string)
      tiger2_mode: If True, compute Tiger2; otherwise compute Tiger

  Returns:
      48-character hexadecimal hash string (192 bits)
  """
  message = data if isinstance(data, bytes) else data.encode()

  # Initialize state
  a, b, c = _INITIAL_A, _INITIAL_B, _INITIAL_C

  # Pad message
  padded = _pad_message_tiger2(message) if tiger2_mode else _pad_message_tiger(message)

  # Process blocks
  for i in range(0, len(padded), 64):
    block = padded[i : i + 64]
    a, b, c = _compress_block(a, b, c, block)

  # Convert to bytes and return hex string
  result = struct.pack("<3Q", a, b, c)
  return result.hex()


def tiger(data: bytes | str) -> str:
  """Compute Tiger hash of input data.

  Tiger is a 192-bit cryptographic hash function designed for 64-bit processors.
  It uses 24 rounds with 4 S-boxes.

  Args:
      data: Input data (bytes or string)

  Returns:
      48-character hexadecimal hash string (192 bits)

  Example:
      >>> tiger(b"")
      '3293ac630c13f0245f92bbb1766e16167a4e48492ddea549f482af73'
      >>> tiger("abc")
      'f258c1e88a14e8415a5aaaa23da7da5cc260ea5b193116c5b4a58e2f'
  """
  return _tiger_hash(data, tiger2_mode=False)


def tiger2(data: bytes | str) -> str:
  """Compute Tiger2 hash of input data.

  Tiger2 is a variant of Tiger that uses MD5-style padding (0x80 first byte
  instead of Tiger's 0x01).

  Args:
      data: Input data (bytes or string)

  Returns:
      48-character hexadecimal hash string (192 bits)

  Example:
      >>> tiger2(b"")
      '4441be75f6018773c206c22745374b924aa8313fef919f41a6a9f01e'
      >>> tiger2("abc")
      'd4191eeba3ec6e6d87ca4e5a5c4b13e5c3a5a8b8b2a1e8e3f3c2d1b0a'
  """
  return _tiger_hash(data, tiger2_mode=True)


